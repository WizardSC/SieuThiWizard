    private void btnNhapHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNhapHangMouseClicked
        if (txtMaPN.getText().equals("")) {
            JOptionPane.showMessageDialog(btnNhapHang, "Vui lòng nhập mã phiếu nhập", "THÔNG BÁO", JOptionPane.ERROR_MESSAGE);

        } else {
            String MaPN = txtMaPN.getText();
            String MaSP = txtMaSP.getText();
            String TenSP = txtTenSP.getText();
            int SoLuong = Integer.parseInt(txtSoLuong.getText());
            int DonGia = Integer.parseInt(txtDonGia.getText());
            int ThanhTien = SoLuong * DonGia;
            boolean flag = true;
            for (ChiTietPNDTO ctpn : dsctpn) {
                if (ctpn.getMaSP().equals(MaSP)) {
                    int old = ctpn.getSoLuong();
                    ctpn.setSoLuong(old + SoLuong);
                    int new1 = ctpn.getSoLuong();
                    ctpn.setThanhTien(new1 * DonGia);
                    flag = false;
                    break;
                }

            }
            if (flag) {
                dsctpn.add(new ChiTietPNDTO(MaPN, MaSP, TenSP, SoLuong, DonGia, ThanhTien));

            }
            outModel(dtmHangChoNhap, dsctpn);
            txtTongTien.setText(String.valueOf(sumPN()));
            txtMaSP.setText("");
            txtTenSP.setText("");
            txtSoLuong.setText("");
            txtDonGia.setText("");
            txtMaLoai.setText("");
            txtMaNSX.setText("");
            txtThanhTien.setText("");
            cbxDonViTinh.setSelectedIndex(0);
        }


    }//GEN-LAST:event_btnNhapHangMouseClicked

