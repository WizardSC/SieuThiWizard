    private void btnTimKiemPNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimKiemPNMouseClicked
        String TuKhoa = txtTuKhoainDSPN.getText();
        String TongTienBD = txtTongTienBDinDSPN.getText();
        String TongTienKT = txtTongTienKTinDSPN.getText();
        if (rdbThongThuonginDSPN.isSelected()) {
            if (TuKhoa.equals("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập thông tin cần tìm", "Thông báo", JOptionPane.WARNING_MESSAGE);

            } else {
                if (rdbMaPNinDSPN.isSelected()) {
                    ArrayList<PhieuNhapDTO> dspn = pnBUS.timKiemTheoMaPN(TuKhoa);
                    showAllDSPN(dspn);
                }
                if (rdbMaNCCinDSNCC.isSelected()) {
                    ArrayList<PhieuNhapDTO> dspn = pnBUS.timKiemTheoMaNCC(TuKhoa);
                    showAllDSPN(dspn);
                }
                if (rdbMaNVinDSNCC.isSelected()) {
                    ArrayList<PhieuNhapDTO> dspn = pnBUS.timKiemTheoMaNV(TuKhoa);
                    showAllDSPN(dspn);
                }
            }

        }
        if (rdbNangCaoinDSPN.isSelected()) {
            if (TongTienBD.equals("") || TongTienKT.equals("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập thông tin cần tìm", "Thông báo", JOptionPane.WARNING_MESSAGE);

            } else {
                ArrayList<PhieuNhapDTO> dspn = pnBUS.timKiemTongTienNangCao(Integer.parseInt(TongTienBD), Integer.parseInt(TongTienKT));
                showAllDSPN(dspn);
            }
        }
    }//GEN-LAST:event_btnTimKiemPNMouseClicked

