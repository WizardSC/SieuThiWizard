    private void btnInPhieuNhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInPhieuNhapMouseClicked
        //dành cho xuất excel
        String MaPN = txtMaPN.getText();
        String MaNCC = txtMaNCC.getText();
        String MaNV = txtMaNV.getText();
        String NgayLap = ((JTextField) txtNgayLap.getDateEditor().getUiComponent()).getText();
        String TongTien = txtTongTien.getText();
        //dành cho xuất excel
       
        for (ChiTietPNDTO ctpn : dsctpn) { //duyệt các phiếu nhập trong hàng chờ nhập
            int SoLuongNhap = ctpn.getSoLuong();
            String MaSP = ctpn.getMaSP(); //lưu số lượng nhập và mã sp của từng món trong hàng chờ nhập vừa nãy 
            spBUS.docDanhSach(); //đọc danh sách sản phẩm
            ArrayList<SanPhamDTO> dssp = spBUS.getListSanPham();
            for (SanPhamDTO sp : dssp) { //duyệt từng sản phẩm trong data sản phẩm
                if (MaSP.equals(sp.getMaSP())) { //nếu mã sản phẩm trong các phiếu nhập trùng với mã sp trong dât sản phẩm
                    int SoLuongTrongKho = sp.getSoLuong(); //lưu lại số lượng hiện tại của sản phẩm đó
                    spBUS.capNhatSoLuongPN(MaSP, SoLuongNhap, SoLuongTrongKho); //cập nhật số lượng mới cho SP
                }
            }

            ctpnBUS.add(ctpn); //thêm chi tiết phiếu nhập vào data
        }
        //dành cho xuất excel
        PhieuNhapDTO pn = new PhieuNhapDTO(MaPN, MaNCC, MaNV, NgayLap, TongTien);
        XuatPhieuNhapBUS xuatphieunhap = new XuatPhieuNhapBUS(pn, dsctpn);
        xuatphieunhap.print();
        //dành cho xuất excel
        dtmHangChoNhap.setRowCount(0);
        dsctpn.clear();
        loadDataWareHouse();
        
    }//GEN-LAST:event_btnInPhieuNhapMouseClicked

