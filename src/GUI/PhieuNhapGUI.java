    public int sumPN() {
        int sum = 0;
        for (ChiTietPNDTO ctpn : dsctpn) {
            int SoLuong = ctpn.getSoLuong();
            int DonGia = ctpn.getDonGia();
            sum += SoLuong * DonGia;
        }
        return sum;
    }

